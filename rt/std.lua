std_pcall = func(function(f)
	-- > func, p1, p2, ...
	-- < true, p1, p2, ...
	-- modify datastack: 2, 0, framesz, retc, base+1
	local a, valvec, base = f:locals(i32, 3)
	f:loadg(oluastack)
	f:i32load(coro.stack)
	f:i32load(buf.ptr)
	f:tee(valvec)
	f:loadg(oluastack)
	f:i32load(coro.data)
	f:tee(a)
	f:i32load(buf.ptr)
	f:load(a)
	f:i32load(buf.len)
	f:add()
	f:i32(dataframe.sizeof)
	f:sub()
	f:tee(a)
	-- TODO load localc from p0 & assign to dataframe

	f:i32load(dataframe.base)
	f:tee(base)
	f:add()
	f:i32(TRUE)
	f:i32store(vec.base)

	f:load(a)
	f:i32(calltypes.prot)
	f:i32store8(dataframe.type)

	f:load(a)
	f:i32(0)
	f:i32store(dataframe.pc)

	f:load(a)
	f:load(a)
	f:i32load(dataframe.retb)
	f:i32(4)
	f:add()
	f:i32store(dataframe.retb)
end)

std_select = func(function(f)
	-- L.stack[base] == '#' ? L.stack.len - base - 1 : L.stack[base + L.stack[base]]
	local a, valstack, base, p0 = f:locals(i32, 4)

	-- base = L.data.base
	-- a = L.stack + base
	f:loadg(oluastack)
	f:i32load(coro.stack)
	f:tee(valstack)
	f:i32load(buf.ptr)
	f:loadg(oluastack)
	f:i32load(coro.data)
	f:tee(a)
	f:i32load(buf.ptr)
	f:load(a)
	f:i32load(buf.len)
	f:add()
	loadstrminus(f, 4)
	f:tee(base)
	f:add()
	f:tee(p0)
	f:i32load8u(obj.type)
	f:i32(types.str)
	f:eq()
	f:iff(function()
		assert(str.base & 7 ~= 0)
		f:load(p0)
		f:i32load8u(str.base)
		f:i32(string.byte('#'))
		f:eq()
		f:load(p0)
		f:i32load(str.len)
		f:i32(1)
		f:eq()
		f:band()
		f:iff(function()
			f:load(valstack)
			f:i32load(buf.ptr)
			f:load(valstack)
			f:i32load(buf.len)
			f:load(base)
			f:sub()
			f:i32(1)
			f:sub()
			f:add()
			f:i32load(vec.base)
			f:i64extends()
			f:call(newi64)
			-- TODO truncate based on base
			f:i32(4)
			f:call(setnthtmp)
			f:ret()
		end)
	end)

	f:load(p0)
	f:call(toint)
	f:tee(p0)
	f:iff(function()
		-- TODO bounds check
		f:loadg(oluastack)
		f:i32load(coro.stack)
		f:tee(a)
		f:i32load(buf.ptr)
		f:load(base)
		f:add()
		f:i32(vec.base)
		f:add()

		f:load(a)
		f:i32load(buf.ptr)
		f:load(base)
		f:load(p0)
		f:i32load(num.base)
		f:add()
		f:add()
		f:i32(vec.base)
		f:add()

		f:load(a)
		f:i32load(buf.len)
		f:load(base)
		f:sub()
		f:tee(p0)
		f:call(memcpy4)

		f:load(a)
		f:load(p0)
		f:i32store(buf.len)

	end, function()
		f:unreachable()
	end)
end)

std_type = func(function(f)
	f:block(i32, function(res)
		f:switch(function()
			f:i32(4)
			f:call(nthtmp)
			f:i32load8u(obj.type)
		end, types.int, types.float, function()
			f:i32(GS.number)
			f:br(res)
		end, types.bool, function()
			f:i32(GS.boolean)
			f:br(res)
		end, types.nul, function()
			f:i32(GS['nil'])
			f:br(res)
		end, types.tbl, function()
			f:i32(GS.table)
			f:br(res)
		end, types.str, function()
			f:i32(GS.string)
			f:br(res)
		end, types.vec, types.buf, function()
			f:i32(GS.userdata)
			f:br(res)
		end, types.functy, function()
			f:i32(GS['function'])
			f:br(res)
		end, types.coro)
		f:i32(GS.thread)
	end)
	f:i32(4)
	f:call(setnthtmp)
end)

std_getmetatable = func(function(f)
	local a, b = f:locals(i32, 2)

	f:i32(4)
	f:call(nthtmp)
	f:tee(a)
	f:i32load8u(obj.type)
	f:tee(b)
	f:i32(types.tbl)
	f:eq()
	f:iff(i32, function()
		f:load(a)
		f:i32load(tbl.meta)
		f:tee(a)
		f:i32(GS.__metatable)
		f:call(tblget)
		f:tee(b)
		f:load(a)
		f:load(b)
		f:select()
	end, function()
		f:loadg(ostrmt)
		f:i32(NIL)
		f:load(b)
		f:i32(types.str)
		f:eq()
		f:select()
	end)
	f:i32(4)
	f:call(setnthtmp)
end)

std_setmetatable = func(function(f)
	local a, b = f:locals(i32, 2)

	f:i32(8)
	f:call(nthtmp)
	f:tee(a)
	f:i32load8u(obj.type)
	f:i32(types.tbl)
	f:eq()
	f:iff(function()
		f:i32(4)
		f:call(nthtmp)
		f:tee(b)
		f:i32load8u(obj.type)
		f:i32(types.tbl)
		f:eq()
		f:iff(function()
			f:load(a)
			f:load(b)
			f:i32store(tbl.meta)
			f:call(tmppop)
			f:ret()
		end)
	end)
	f:unreachable()
end)
